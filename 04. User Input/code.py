# Taking user input with a prompt
# The text inside input() is displayed before capturing input
a = input("Enter your name: ")
print("My name is", a)

# All user inputs are strings by default
x = input("Enter first number: ")
y = input("Enter second number: ")

# String concatenation: inputs are combined as strings
print(x + y)

# Type casting: convert strings to integers for arithmetic addition
print(int(x) + int(y))
